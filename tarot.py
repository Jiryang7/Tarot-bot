"""
타로 수비학 계산 및 카드 정보 모듈
- 연도+월+일 합산 → 각 자리 합산 → 22 초과 시 한 번 더 각 자리 합산
- 22가 나오면 → 0번 (바보) 카드로 처리
- 카드 범위: 0번, 2~21번 (1번 제외, 총 21종)
"""


def calculate_destiny_card(birth_clean: str) -> int:
    year  = int(birth_clean[:4])
    month = int(birth_clean[4:6])
    day   = int(birth_clean[6:8])
    total = year + month + day
    result = sum(int(c) for c in str(total))
    if result > 22:
        result = sum(int(c) for c in str(result))
    if result == 22:
        result = 0
    return result


