def get_card_info(card_number: int) -> dict:
    """
    카드 번호로 카드 정보 반환
    - 0번: 바보 (22가 나왔을 때)
    - 없는 번호는 0번으로 fallback
    """
    return CARDS.get(card_number, CARDS[0])


def get_compatibility(card1: int, card2: int) -> dict:
    """
    두 카드의 관계 정보 반환
    ※ 실제 카드 간 관계 내용은 추후 직접 입력 예정
    """
    key = (min(card1, card2), max(card1, card2))
    description = COMPATIBILITY.get(key, "")
    return {
        "card1": card1,
        "card2": card2,
        "description": description,
    }


